let private listingBlocksMsg page count blocksCount msg =
  let m s =
    match msg with
    | ListingBlocks.Started ->
      s

    | ListingBlocks.Error ->
      c$"–ù–µ–∏–∑–≤–µ—Å—Ç–Ω–∞—è –∫–æ–º–∞–Ω–¥–∞. {randomEmoji ()}

        {s}"

  let min = page * count + 1
  let max = page * count + blocksCount

  if min = max
  then $"–ë–ª–æ–∫ ‚Ññ {min}"
  else $"–ë–ª–æ–∫–∏ —Å ‚Ññ {min} –ø–æ ‚Ññ {max}"
  |> m
  |> c

